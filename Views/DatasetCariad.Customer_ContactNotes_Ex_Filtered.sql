CREATE VIEW [DatasetCariad].[Customer_ContactNotes_Ex_Filtered]
AS
SELECT        MIG_SITE_NAME, CUSTOMER_ID, NOTE_DATE, ISNULL(CONTACT_NAME, '') AS CONTACT_NAME, NOTE_SHORT, NOTE_LONG, ISNULL(NOTE_REASON, '') AS NOTE_REASON, CONTACT_USER
FROM            OPENQUERY(CARIAD, 
                         'select ''GBCA1'' AS MIG_SITE_NAME,

CF$_CUSTOMER_ID AS CUSTOMER_ID,
cf$_timestamp as NOTE_DATE,
'''' as CONTACT_NAME,
CF$_CASE_TITLE AS Note_Short,
CF$_COMMENT AS Note_Long,
CF$_CASE_NOTES   AS Note_Reason,
CF$_USER AS CONTACT_USER

FROM CUSTOMER_JOURNAL_CLV
')
                          AS derivedtbl_1
WHERE        (NOT EXISTS
                             (SELECT        1 AS Expr1
                               FROM            Dataset.Customer_Filter_Override AS F
                               WHERE        (MIG_SITE_NAME = 'GBCA1') AND (CUSTOMER_ID = derivedtbl_1.CUSTOMER_ID) AND (IsAlwaysExcluded = 1)))
